def P(text, s=body_style):
    return Paragraph(text, s)

def make_table(header, rows, col_widths):
    data = [[Paragraph(h, tbl_header_style) for h in header]]
    for r in rows:
        data.append([Paragraph(str(c), tbl_cell_style) for c in r])
    t = Table(data, colWidths=col_widths, repeatRows=1)
    t.setStyle(TableStyle([
        ("BACKGROUND", (0,0), (-1,0), colors.HexColor("#1a2b4c")),
        ("GRID", (0,0), (-1,-1), 0.5, colors.HexColor("#cccccc")),
        ("ROWBACKGROUNDS", (0,1), (-1,-1), [colors.white, colors.HexColor("#f4f6fa")]),
        ("VALIGN", (0,0), (-1,-1), "MIDDLE"),
        ("TOPPADDING", (0,0), (-1,-1), 4),
        ("BOTTOMPADDING", (0,0), (-1,-1), 4),
    ]))
    return t

def bullets(items):
    return ListFlowable(
        [ListItem(P(it, bullet_style), leftIndent=6, bulletColor=colors.HexColor("#1a2b4c")) for it in items],
        bulletType="bullet", start="•", leftIndent=14, spaceBefore=2, spaceAfter=6
    )

def section_header(num, text):
    return Paragraph(f"{num}. {text}", h2_style)

def company_block(name, ticker, verdict_tag, verdict_color, momentum_intro, table_header,
                   table_rows, col_widths, momentum_notes, driver_items, judgment_text,
                   risk_items):
    flow = []
    flow.append(HRFlowable(width="100%", thickness=1.2, color=colors.HexColor("#1a2b4c"), spaceAfter=6))
    flow.append(Paragraph(f"{name} <font color='#888888' size='10'>({ticker})</font>", company_title_style))
    tag_style = style("TagK", fontName="MalgunBold", fontSize=9.5, leading=13,
                       textColor=colors.white, backColor=verdict_color,
                       borderPadding=(4,6,4,6), alignment=0)
    flow.append(Paragraph(verdict_tag, tag_style))
    flow.append(Spacer(1, 6))

    flow.append(section_header("1", "최근 실적 모멘텀"))
    flow.append(P(momentum_intro))
    flow.append(Spacer(1, 3))
    flow.append(make_table(table_header, table_rows, col_widths))
    flow.append(Spacer(1, 4))
    if momentum_notes:
        flow.append(P(momentum_notes))

    flow.append(section_header("2", "성장 동력의 실체"))
    flow.append(bullets(driver_items))

    flow.append(section_header("3", "구조적 성장 vs 일시적 반등 판단"))
    flow.append(Paragraph(judgment_text, verdict_style))

    flow.append(section_header("4", "주요 리스크"))
    flow.append(bullets(risk_items))
    flow.append(Spacer(1, 14))
    return flow

